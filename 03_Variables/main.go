package main

import "fmt"

func main() {
	//Variables
	var name = "golang"
	fmt.Println(name)

	var isAdult = true
	fmt.Println(isAdult)

	var age = 25
	fmt.Println(age)

	var height = 1.8
	fmt.Println(height)

	Adult := true //short hand declaration
	fmt.Println(Adult)

	var isYes bool = true //Long hand declaration by specifying a particular data type
	fmt.Println(isYes)
}
